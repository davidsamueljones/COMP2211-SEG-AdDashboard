package group33.seg.model.configs;

/**
 * Structure-like class for constructing a generic graph. All variables are public to allow
 * for easy structure access.
 */
public abstract class GraphConfig {
  
  /** Storage identifier of graph */
  public String identifier;
  
  /** Main chart title */
  public String title;
  
  /** Title of X Axis */
  public String xAxisTitle;
  
  /** Title of Y Axis */
  public String yAxisTitle;

}
